package com.bkav.musicapplication.enumdefine;

public enum MediaStatus {
    REPEAT_ALL, //Repeat all list song
    REPEAT_ONE, //Repeat only one song
    SHUFFLE,    //Shuffle
    REPEAT_AND_SHUFFLE,     //Shuffle when next song and repeat all list song
    REPEAT_ONE_AND_SHUFFLE, //Repeat only one song and shuffle when click next song
    NONE    //No shuffle, no repeat all list song.
}
